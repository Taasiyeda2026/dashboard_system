let scheduled = false;
let processing = false;

function replaceExactText(root) {
  const walker = document.createTreeWalker(root, NodeFilter.SHOW_TEXT);
  const textNodes = [];
  while (walker.nextNode()) textNodes.push(walker.currentNode);

  textNodes.forEach((node) => {
    const current = node.nodeValue?.trim();
    const replacement = TEXT_REPLACEMENTS.get(current);
    if (!replacement) return;
    const leading = node.nodeValue.match(/^\s*/)?.[0] || '';
    const trailing = node.nodeValue.match(/\s*$/)?.[0] || '';
    node.nodeValue = `${leading}${replacement}${trailing}`;
  });
}

function simplifyStatuses(root) {
  root.querySelectorAll('.ar2-status').forEach((status) => {
    const completed = status.classList.contains('is-complete');
    const label = completed ? 'הושלם' : 'טרם הושלם';
    if (status.textContent !== label) status.textContent = label;
  });
}

function removeExplanations(root) {
  root.querySelectorAll('.ar2-card__head .ar2-muted').forEach((node) => {
    if (!node.closest('header.ar2-card')) node.remove();
  });

  root.querySelectorAll('.ar2-private, .ar2-guide').forEach((node) => node.remove());

  root.querySelectorAll('.ar2-card > .ar2-muted').forEach((node) => {
    if (node.textContent.includes('חלק העובד:') || node.textContent.includes('חלק המנהל:')) node.remove();
  });
}

function refineHeader(root) {
  const header = root.querySelector('main.ar2-body > header.ar2-card');
  const metadata = header?.querySelector('.ar2-card__head .ar2-muted');
  if (metadata) {
    const lines = metadata.innerHTML.split('<br>').filter((line) => !line.trim().startsWith('תפקיד:'));
    metadata.innerHTML = lines.join('<br>');
  }

  root.querySelectorAll('.ar2-signatures span').forEach((item) => {
    const text = item.textContent.trim();
    if (text === 'סיום המשוב: —') {
      item.remove();
      return;
    }
    if (text.startsWith('שם העובד/ת:')) item.textContent = text.replace('שם העובד/ת:', 'עובד/ת:');
    if (text.startsWith('שם המנהל:')) item.textContent = text.replace('שם המנהל:', 'מנהל:');
    if (text.startsWith('סיום המשוב:')) item.textContent = text.replace('סיום המשוב:', 'מועד השלמה:');
  });
}

function refineAcknowledgement(root) {
  root.querySelectorAll('.ar2-check span').forEach((node) => {
    const current = node.textContent.trim();
    if (!current.startsWith('קראתי את המשוב ואת סיכום המנהל')) return;
    node.textContent = 'קראתי את המשוב ואת הסיכום הניהולי ואני מאשר/ת את קבלתם. האישור אינו מעיד בהכרח על הסכמה לכל האמור.';
  });
}

function processAnnualReviews() {
  if (processing) return;
  const roots = document.querySelectorAll('.ar2-screen, [data-ar2-landing]');
  if (!roots.length) return;

  processing = true;
  try {
    roots.forEach((root) => {
      refineHeader(root);
      replaceExactText(root);
      simplifyStatuses(root);
      removeExplanations(root);
      refineAcknowledgement(root);
    });
  } finally {
    processing = false;
  }
}

function scheduleProcess() {
  if (scheduled) return;
  scheduled = true;
  queueMicrotask(() => {
    scheduled = false;
    processAnnualReviews();
  });
}

new MutationObserver(scheduleProcess).observe(document.documentElement, {
  childList: true,
  subtree: true
});

scheduleProcess();
